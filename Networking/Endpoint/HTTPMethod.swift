/// Represents HTTP methods.
public enum HTTPMethod: String {
    /// The GET method for requesting data.
    case GET
    
    /// The POST method for sending data.
    case POST
    
    /// The DELETE method for removing data.
    case DELETE
    
    /// The PATCH method for updating data.
    case PATCH
}
